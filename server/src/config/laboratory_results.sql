CREATE TABLE laboratory_results (
  id INT AUTO_INCREMENT PRIMARY KEY,
  patient_name VARCHAR(255) NOT NULL,
  age INT,
  sex VARCHAR(10),
  date_taken DATE DEFAULT (CURRENT_DATE),

  fbs VARCHAR(20),
  bun VARCHAR(20),
  creatinine VARCHAR(20),
  uric_acid VARCHAR(20),
  cholesterol VARCHAR(20),
  triglycerides VARCHAR(20),
  hdl VARCHAR(20),
  ldl VARCHAR(20),
  vldl VARCHAR(20),
  sodium VARCHAR(20),
  potassium VARCHAR(20),
  chloride VARCHAR(20),
  calcium VARCHAR(20),
  sgot VARCHAR(20),
  sgpt VARCHAR(20),
  rbs VARCHAR(20),

  alk_phosphatase VARCHAR(20),
  total_protein VARCHAR(20),
  albumin VARCHAR(20),
  globulin VARCHAR(20),
  ag_ratio VARCHAR(20),
  total_bilirubin VARCHAR(20),
  direct_bilirubin VARCHAR(20),
  indirect_bilirubin VARCHAR(20),
  ionised_calcium VARCHAR(20),
  magnesium VARCHAR(20),
  hba1c VARCHAR(20),
  ogtt_30min VARCHAR(20),
  ogtt_1hr VARCHAR(20),
  ogtt_2hr VARCHAR(20),
  ppbs_2hr VARCHAR(20),
  inor_phosphorus VARCHAR(20)
);
